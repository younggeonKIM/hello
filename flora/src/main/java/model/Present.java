package model;

public class Present {
	private int pres_seq;
	private String floname;
	private String mok;
	private String gwa; 
	private String sok;
	private String flomsg;
	
	public int getPres_seq() {
		return pres_seq;
	}
	public void setPres_seq(int pres_seq) {
		this.pres_seq = pres_seq;
	}
	public String getFloname() {
		return floname;
	}
	public void setFloname(String floname) {
		this.floname = floname;
	}
	public String getMok() {
		return mok;
	}
	public void setMok(String mok) {
		this.mok = mok;
	}
	public String getGwa() {
		return gwa;
	}
	public void setGwa(String gwa) {
		this.gwa = gwa;
	}
	public String getSok() {
		return sok;
	}
	public void setSok(String sok) {
		this.sok = sok;
	}
	public String getFlomsg() {
		return flomsg;
	}
	public void setFlomsg(String flomsg) {
		this.flomsg = flomsg;
	}
	
	
}
